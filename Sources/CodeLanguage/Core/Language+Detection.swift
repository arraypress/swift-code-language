    /// Detects the language of the file at `url` from its name and extension.
    /// Returns ``plainText`` when the file type is unrecognized. Never reads file contents.
    static func detect(for url: URL) -> Language {
        detect(filename: url.lastPathComponent)
    }

    /// Detects the language from a bare filename (e.g. `"main.swift"`, `"Makefile"`, `".env"`).
    static func detect(filename: String) -> Language {
        let name = filename.lowercased()
        if let l = Self.filenameMap[name] { return l }
        if name == "dockerfile" || name.hasPrefix("dockerfile.") { return .dockerfile }
        if name.hasPrefix(".env") { return .dotenv }
        for (ext, lang) in Self.compoundExtensionMap where name.hasSuffix("." + ext) { return lang }
        if let dot = name.lastIndex(of: "."), dot < name.index(before: name.endIndex) {
            let ext = String(name[name.index(after: dot)...])
            if let l = Self.extensionMap[ext] { return l }
        }
        return .plainText
    }

